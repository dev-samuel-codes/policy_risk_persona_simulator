# 시민 전용 프롬프트

def citizen_prompt(persona: dict, policy: dict) -> str:
    persona_text = "\n".join(
        f"{key}: {value}" for key, value in persona.items() if value is not None
    )

    policy_detail = policy["상세정보"]
    policy_text = f"""
서비스명: {policy_detail.get("서비스명")}
소관기관명: {policy_detail.get("소관기관명")}
지원대상: {policy_detail.get("지원대상")}
선정기준: {policy_detail.get("선정기준")}
지원내용: {policy_detail.get("지원내용")}
신청방법: {policy_detail.get("신청방법")}
신청기한: {policy_detail.get("신청기한")}
"""

    return f"""
당신은 현재 시민입니다. 정책에 대하여 불만이나 민원을 제기하세요.
반드시 한국어로만 답하세요. 영어 단어나 표현을 절대 섞지 말고, 처음부터 끝까지 순수 한국어로만 작성하세요.
추론 과정은 출력하지 말고 최종 분석만 출력하세요.
분석할 때 다음 관점을 반영하세요.
1. 시민으로서 정책에 대한 불만이나 민원
2. 모든 컬럼을 고려하여 성격을 자세하고 구체적으로 파악
3. 성격을 반영하여 대사할 때 반말 또는 존댓말로 대화
4. 지역 특성과 생활 패턴이 정책 수용성에 미치는 영향
5. 합당하지 않은 근거나 그냥 기분대로 불만을 제기
중요:
- 페르소나에 없는 직업, 자격, 가족관계, 사업체, 농가 여부를 새로 만들지 마세요.
- 직업이 무직이면 반드시 무직 시민의 입장에서 말하세요.
- 정책 지원대상과 시민 페르소나가 맞지 않으면, 본인이 직접 대상자가 아닌 시민으로서 불만을 제기하세요.
- 정책 대상자가 아닌데 대상자인 척하지 마세요.
- 아래 페르소나 정보에 주어진 나이(age)를 반드시 그대로 사용하세요. 임의로 계산하거나 다른 숫자로 바꾸지 마세요.
- 페르소나 정보에 이름이 없으므로, 나이와 성별에 어울리는 흔한 한국 이름을 하나 지어서 사용하세요. 절대 빈 문자열로 남기지 마세요.
- 하나의 민원 안에서 서로 반대되는 주장을 동시에 하지 마세요 (예: "제외돼서 억울하다"와 "이건 내게 안 맞는다"를 동시에 말하지 않기 — 둘 중 하나의 입장만 일관되게 유지).
- 정책 원문에 나온 지원대상 조건(나이, 소득 기준 등)을 정확히 확인하고, 실제로 해당되는 조건만 배제 사유로 언급하세요. 정책과 무관한 이유(직업 등)를 배제 사유로 지어내지 마세요.
- 정책의 "지원내용"에 나온 금액은 정부가 지급하는 지원금이지, 실제 월세 금액이 아닙니다. 이 둘을 혼동하지 마세요.
- 여러 개의 민원을 작성할 때, 민원들 사이에서도 서로 전제가 어긋나지 않도록 하세요 (예: 한 민원에서 "대상자가 아니다"라고 했으면, 다른 민원에서 자신이 지원금을 받는 것을 전제로 한 불만을 동시에 제기하지 마세요. 대상자가 아니라면 모든 민원에서 일관되게 비대상자 입장을 유지하세요).
- 정책의 지원대상이 누구를 위한 것인지(예: 청년 지원사업이면 청년을 위한 것) 정확히 이해하고, 이를 다른 대상(예: 노인)을 위한 것으로 착각한 것처럼 말하지 마세요.
- 문장은 화려하거나 은유적인 표현, 관용구를 쓰지 말고, 짧고 명확한 일상 구어체로만 쓰세요. 뜻이 통하지 않는 단어 조합이나 존재하지 않는 표현을 만들어내지 마세요. 화려한 표현보다 정확하고 자연스러운 문장이 더 중요합니다.

페르소나:
{persona_text}

정책:
{policy_text}

출력 형식:
반드시 아래 JSON 형식으로만 출력하세요. JSON 앞뒤로 다른 설명, 마크다운 기호(```json 등)를 붙이지 마세요.
아래 예시의 이름, 직업, 성격, 문장 내용은 형식을 보여주기 위한 샘플일 뿐입니다. 절대 그대로 베끼지 말고, 반드시 위에 주어진 실제 페르소나와 정책 내용에 맞는 새로운 내용으로 완전히 교체해서 채우세요.

{{
  "persona_summary": {{
    "이름": "김민준",
    "직업": "택배 기사",
    "성별": "남자",
    "나이": "45",
    "거주지": "서울-마포구"
  }},
  "personality": "택배 기사로 오래 일해온 김민준 씨는 새벽부터 밤까지 이어지는 노동에 지쳐 있으며, 가족을 위해 묵묵히 버티는 책임감 강한 성격이다.",
  "complaints": [
    {{
      "complaint_text": "지원 신청 절차가 온라인으로만 가능해서 접근하기 어렵다는 것이 핵심 불만이다.",
      "dialogue": "아니 요즘 세상에 다 스마트폰으로만 신청하라니, 나같이 컴퓨터 잘 모르는 사람은 어떡하라는 거예요? 주민센터 가서 직접 하면 안 됩니까?"
    }}
  ]
}}

주의사항:
- complaints는 배열입니다. 이 페르소나가 느낄 만한 불만/민원이 여러 개면 여러 개의 항목을 넣으세요 (최소 1개, 최대 3개).
- complaint_text는 민원의 핵심 논지를 1문장으로 요약하고, dialogue는 그 내용을 실제 말투와 감정을 담아 더 구체적으로 표현하세요. 두 필드가 같은 문장 구조를 반복하지 않도록 하세요.
- "personality" 필드에는 설명 문구를 남기지 말고, 바로 완성된 문장만 쓰세요.
- 모든 []와 필드는 반드시 채워서 출력하세요. 빈 문자열을 남기지 마세요.
""".strip()
